a=float(input("enter percentage: "))
if 90<a<=100 :
    print("A+ grade")
elif 80<a<=90 :
    print("A grade")
elif 70<a<=80 :
    print("B+ grade")
elif 60<a<=70 :
    print("B grade")
elif 50<a<=60 :
    print("C+ grade")
elif 34<a<=50 :
    print("C grade")
elif 0<a<=34 :
    print("fail")
else :
    print("invalid")
    